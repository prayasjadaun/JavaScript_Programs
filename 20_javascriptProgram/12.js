//Question-12--JavaScript Program to Demonstrate the Use of var, const, and let Keywords

// var keyword (function-scoped)
var variableVar = 10;

// const keyword (block-scoped, immutable)
const variableConst = 20;

// let keyword (block-scoped, mutable)
let variableLet = 30;

console.log(`var: ${variableVar}, const: ${variableConst}, let: ${variableLet}`);
